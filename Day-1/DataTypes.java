
public class DataTypes {
    public static void main(String[] args)
    {
        //All The Whole Numbers  Data Types
        byte b = 127; //-127 to 127
        int x = 2000000000; 
        short y = 30;
        long z = 4000000000000000000L;
        System.out.println("Byte : " +b);
        System.out.println("Short : " +y);
        System.out.println("Integer : " +x);
        System.out.println("Long: " +z);

        //All Of The Decimals DataTypes
        // By Default it is float
        double d = 233232.31323;
        float f = 43433.323f;
        System.out.println("Double " +d);

        System.out.println("Float " +f);

        //boolean datatype 

        boolean bb = true;
        System.out.println("Boolean "+bb);

        // String Data Types Or Single Char
        char c = 'c';
        System.out.println("Char " +c);
        String v = "Faisal";
        System.out.println("String " +v);

    }

}
